""" В настольной игре Скрабл (Scrabble) каждая буква имеет определенную
ценность. В случае с английским алфавитом очки распределяются так:
● A, E, I, O, U, L, N, S, T, R – 1 очко;
● D, G – 2 очка;
● B, C, M, P – 3 очка;
● F, H, V, W, Y – 4 очка;
● K – 5 очков;
● J, X – 8 очков;
● Q, Z – 10 очков.
А русские буквы оцениваются так:
● А, В, Е, И, Н, О, Р, С, Т – 1 очко;
● Д, К, Л, М, П, У – 2 очка;
● Б, Г, Ё, Ь, Я – 3 очка;
● Й, Ы – 4 очка;
● Ж, З, Х, Ц, Ч – 5 очков;
● Ш, Э, Ю – 8 очков;
● Ф, Щ, Ъ – 10 очков.
Напишите программу, которая вычисляет стоимость введенного пользователем слова.
Будем считать, что на вход подается только одно слово, которое содержит либо только
английские, либо только русские буквы.
 """
 
 
def calculate_word_score(word):
    english_scores = {
        'A': 1, 'E': 1, 'I': 1, 'O': 1, 'U': 1,
        'L': 1, 'N': 1, 'S': 1, 'T': 1, 'R': 1,
        'D': 2, 'G': 2, 'K': 5, 'J': 8, 'X': 8,
        'Q': 10, 'Z': 10
    }

    russian_scores = {
        'А': 1, 'Б': 3, 'В': 1, 'Г': 1, 'Д': 2,
        'Е': 1, 'Ё': 1, 'Ж': 5, 'З': 1, 'И': 1,
        'Й': 1, 'К': 2, 'Л': 2, 'М': 2, 'Н': 1,
        'О': 1, 'П': 2, 'Р': 1, 'С': 1, 'Т': 1,
        'У': 2, 'Ф': 10, 'Х': 5, 'Ц': 5, 'Ч': 5,
        'Ш': 8, 'Щ': 8, 'Ъ': 10, 'Ы': 2, 'Ь': 3,
        'Э': 10, 'Ю': 10, 'Я': 10
    }

    score = 0
    word = word.upper()  # Преобразуем слово в верхний регистр

    if all(letter in english_scores for letter in word):
        score = sum(english_scores[letter] for letter in word)
    elif all(letter in russian_scores for letter in word):
        score = sum(russian_scores[letter] for letter in word)

    return score

# Ввод слова пользователем
word = input("Введите слово: ")

# Вызов функции и вывод результата
result = calculate_word_score(word)
print(f"Стоимость слова '{word}': {result} очков.")
